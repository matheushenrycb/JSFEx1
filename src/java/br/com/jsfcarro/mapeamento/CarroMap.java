package br.com.jsfcarro.mapeamento;

/**
 *
 * @author laboratorio
 */
public class CarroMap {

    private String modelo;
    private String marca;
    private int anof;
    private int anom;
    private String obs;

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public int getAnof() {
        return anof;
    }

    public void setAnof(int anof) {
        this.anof = anof;
    }

    public int getAnom() {
        return anom;
    }

    public void setAnom(int anom) {
        this.anom = anom;
    }

    public String getObs() {
        return obs;
    }

    public void setObs(String obs) {
        this.obs = obs;
    }

}
